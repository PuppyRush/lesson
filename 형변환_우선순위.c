
#define _CRT_SECURE_NO_WARNINGS

#include <stdio.h>

int main() {

	int x = 5;
	int y = 3;
	int z = 10;

	printf("%d\n", sizeof(int));

	printf("%0.2d %d\n", x / y * z, x*y / z);
	printf("%0.2f %f\n", x / y * z, x*y / z);
	printf("%0.2f %f\n", (double)x / y * z, (double)x*y / z);
	printf("%0.2f %f\n", x / (double)y * z, x*y / (double)z);
	
	int sum = x++ + ++y * ++z;

	x = 10;
	int sum2 = x++ + ++x + ++x;
	//https://msdn.microsoft.com/ko-kr/library/2bxt6kc4.aspx
	//어셈블리까지 보고 싶다면? http://ehclub.co.kr/431

	printf("%d %d\n", sum, sum2);

	return 0;

}

